def add(n1, n2):
    return n1 + n2

def sub(n1, n2):
    return n1 - n2

def mul(n1, n2):
    return n1 * n2

def dev(n1 , n2):
    return n1 / n2
operations ={
    "+":  add,
    "-":  sub,
    "*":  mul,
    "/":  dev
}
def calcaluter():
    num1 = float(input("pick a number: "))
    should_continue = True
    while should_continue:

        num2 = float(input("pick a another number: "))
        for things in operations:
            print(things)


        operations_symbol = input("pick an operations: ")

        calcluting_value = operations[operations_symbol]
        answer = calcluting_value(num1, num2)

        print(f"{num1} {operations_symbol} {num2} = {answer} ")
        if input(f"Type 'y' to continue calculating with {answer}, or type 'n' to start a new calculation: ") == 'y':
            num1 = answer
        else:
            should_continue = False
            calcaluter()
calcaluter()



